package lovera.comuns.grafoanalise;

public class Espaco {
	
	private double margemInicial;
	private double margemEsquerda;
	
	public double getMargemInicial() {
		return margemInicial;
	}
	public void setMargemInicial(double margemInicial) {
		this.margemInicial = margemInicial;
	}
	public double getMargemEsquerda() {
		return margemEsquerda;
	}
	public void setMargemEsquerda(double margemEsquerda) {
		this.margemEsquerda = margemEsquerda;
	}
}
